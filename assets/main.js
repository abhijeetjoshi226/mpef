  function collectAutoTextNodes() {
    const scope = document.querySelectorAll("main *, footer *, header *");
    scope.forEach((el) => {
      if (!(el instanceof Element)) return;
      if (el.closest("[data-i18n]")) return;
      el.childNodes.forEach((node) => {
        if (node.nodeType !== Node.TEXT_NODE) return;
        const text = node.nodeValue || "";
        const trimmed = text.trim();
        if (!trimmed) return;
        autoTextNodes.push(node);
        autoTextOriginal.set(node, text);
      });
    });
  }

  function applyAutoTextTranslations(lang) {
    autoTextNodes.forEach((node) => {
      const original = autoTextOriginal.get(node);
      if (!original) return;
      const trimmed = original.trim();
      if (!trimmed) return;
      const mr = MR_BY_EN[trimmed];
      if (lang === "mr" && mr) {
        node.nodeValue = original.replace(trimmed, mr);
      } else {
        node.nodeValue = original;
      }
    });
  }

