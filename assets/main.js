  function normalizeI18n(value) {
    if (!value) return value;
    // Repair mojibake like "à¤®à¤°à¤¾à¤ à¥€" -> "मराठी".
    // Some strings are multi-pass encoded, so decode repeatedly until stable.
    let out = String(value);
    const looksBroken = (s) => /[ÃÂàâ€¢€™¤¥]/.test(s);
    for (let i = 0; i < 4; i += 1) {
      if (!looksBroken(out)) break;
      try {
        const next = new TextDecoder("utf-8").decode(
          Uint8Array.from(out, (ch) => ch.charCodeAt(0) & 255)
        );
        if (!next || next === out) break;
        out = next;
      } catch {
        break;
      }
    }
    return out;
  }

